#!/usr/bin/env bash
# Generate the benchmark workspace configs for every runner, so the same task
# definitions stay in lock-step across tsr / npm+bun / just / go-task / make.
#
# Scenarios (see run.sh):
#   startup  one task that spawns `true`            — per-invocation overhead
#   steps5   one task, 5 sequential commands        — in-task sequencing
#   graph5   one task with 5 trivial dependencies   — dependency-graph overhead
#   graph10  one task with 10 trivial dependencies  — graph overhead, scaled
set -euo pipefail

here="$(cd "$(dirname "$0")" && pwd)"
ws="$here/workspace"
mkdir -p "$ws"

STEPS=5
SMALL=5
LARGE=10
MAXS=$LARGE

# --- tsr: tasks.toml ---
{
  echo "# Generated by gen-workspace.sh — do not edit by hand."
  echo
  echo "[tasks.noop]"
  echo 'run = "true"'
  echo
  echo "[tasks.steps5]"
  printf 'run = "%s"\n' "$(yes true | head -n $STEPS | paste -sd' ' - | sed 's/ / \&\& /g')"
  for i in $(seq 1 $MAXS); do
    echo
    echo "[tasks.s$i]"
    echo 'run = "true"'
  done
  echo
  echo "[tasks.shell]"
  # Exercises tsr's mini-shell: $VAR expansion + && sequencing, no shell launched.
  echo 'run = "echo $HOME && echo done"'
  echo
  echo "[tasks.cli]"
  # Calls a locally-installed binary from node_modules/.bin (like vite/eslint).
  echo 'run = "localcli"'
  echo
  echo "[tasks.graph5]"
  echo "deps = [$(seq 1 $SMALL | sed 's/.*/"s&"/' | paste -sd, -)]"
  echo
  echo "[tasks.graph10]"
  echo "deps = [$(seq 1 $LARGE | sed 's/.*/"s&"/' | paste -sd, -)]"
} > "$ws/tasks.toml"

# --- npm / bun: package.json (s1..sN and steps5; graphs are chained in run.sh) ---
{
  echo '{'
  echo '  "name": "tsr-bench-workspace",'
  echo '  "private": true,'
  echo '  "version": "0.0.0",'
  echo '  "scripts": {'
  echo '    "noop": "true",'
  echo '    "shell": "echo $HOME && echo done",'
  echo '    "cli": "localcli",'
  printf '    "steps5": "%s",\n' "$(yes true | head -n $STEPS | paste -sd' ' - | sed 's/ / \&\& /g')"
  for i in $(seq 1 $MAXS); do
    printf '    "s%s": "true"%s\n' "$i" "$([ "$i" -lt "$MAXS" ] && echo , )"
  done
  echo '  }'
  echo '}'
} > "$ws/package.json"

# --- just: justfile ---
{
  echo "# Generated by gen-workspace.sh"
  echo
  printf 'noop:\n    @true\n\n'
  printf 'shell:\n    @echo $HOME && echo done\n\n'
  printf 'steps5:\n'
  for _ in $(seq 1 $STEPS); do printf '    @true\n'; done
  printf '\n'
  for i in $(seq 1 $MAXS); do printf 's%s:\n    @true\n\n' "$i"; done
  printf 'graph5: %s\n\n' "$(seq 1 $SMALL | sed 's/.*/s&/' | paste -sd' ' -)"
  printf 'graph10: %s\n' "$(seq 1 $LARGE | sed 's/.*/s&/' | paste -sd' ' -)"
} > "$ws/justfile"

# --- go-task: Taskfile.yml ---
{
  echo "# Generated by gen-workspace.sh"
  echo 'version: "3"'
  echo
  echo "tasks:"
  echo "  noop:"
  echo "    silent: true"
  echo "    cmds: [true]"
  echo "  shell:"
  echo "    silent: true"
  echo '    cmds: ["echo $HOME && echo done"]'
  echo "  steps5:"
  echo "    silent: true"
  echo "    cmds: [$(yes true | head -n $STEPS | paste -sd, -)]"
  for i in $(seq 1 $MAXS); do
    echo "  s$i:"
    echo "    silent: true"
    echo "    cmds: [true]"
  done
  echo "  graph5:"
  echo "    silent: true"
  echo "    cmds:"
  for i in $(seq 1 $SMALL); do echo "      - task: s$i"; done
  echo "  graph10:"
  echo "    silent: true"
  echo "    cmds:"
  for i in $(seq 1 $LARGE); do echo "      - task: s$i"; done
} > "$ws/Taskfile.yml"

# --- make: Makefile (tabs required for recipes) ---
{
  echo "# Generated by gen-workspace.sh"
  echo ".PHONY: noop shell steps5 graph5 graph10 $(seq 1 $MAXS | sed 's/.*/s&/' | paste -sd' ' -)"
  echo
  printf 'noop:\n\t@true\n\n'
  # $$ escapes to a literal $ for the recipe shell.
  printf 'shell:\n\t@echo $$HOME && echo done\n\n'
  printf 'steps5:\n'
  for _ in $(seq 1 $STEPS); do printf '\t@true\n'; done
  printf '\n'
  for i in $(seq 1 $MAXS); do printf 's%s:\n\t@true\n\n' "$i"; done
  printf 'graph5: %s\n\n' "$(seq 1 $SMALL | sed 's/.*/s&/' | paste -sd' ' -)"
  printf 'graph10: %s\n' "$(seq 1 $LARGE | sed 's/.*/s&/' | paste -sd' ' -)"
} > "$ws/Makefile"

# --- a locally-installed Node CLI in node_modules/.bin ---
# Stands in for vite/eslint (Node programs): the `cli`/`localbin` scenario measures
# tsr vs npm vs bun resolving and launching a project-local binary. Not committed
# (node_modules is gitignored); regenerated here.
mkdir -p "$ws/node_modules/.bin"
cat > "$ws/node_modules/.bin/localcli" <<'EOF'
#!/usr/bin/env node
// Minimal stand-in for a real local CLI (eslint/vite are Node programs too).
process.exit(0);
EOF
chmod +x "$ws/node_modules/.bin/localcli"

echo "generated $ws/{tasks.toml,package.json,justfile,Taskfile.yml,Makefile} + node_modules/.bin/localcli"
